const EMOJI_SEARCH_LABELS: Record<string, string[]> = {
  Smileys: ["smile", "happy", "laugh", "face", "grin", "wink", "love", "cool", "nerd", "think", "sad", "cry", "sleep", "sick"],
  People: ["hand", "wave", "thumbs", "clap", "point", "fist", "pray", "muscle", "brain", "eye", "finger", "ok"],
  Nature: ["dog", "cat", "animal", "flower", "tree", "plant", "sun", "moon", "rainbow", "star", "weather", "rain", "snow"],
  Food: ["fruit", "vegetable", "meat", "bread", "pizza", "burger", "cake", "coffee", "drink", "food", "eat", "cook"],
  Travel: ["car", "plane", "rocket", "house", "building", "city", "mountain", "beach", "map", "world", "earth"],
  Activities: ["sport", "ball", "game", "music", "art", "trophy", "medal", "win", "play", "guitar", "movie"],
  Objects: ["book", "pen", "paper", "computer", "phone", "light", "key", "lock", "tool", "money", "mail", "folder"],
  Symbols: ["heart", "love", "check", "cross", "warning", "star", "fire", "sparkle", "circle", "arrow", "stop"],
  Flags: ["flag", "country", "nation", "usa", "uk", "japan", "france", "germany", "canada", "brazil", "korea", "india", "philippines"],
};

export function searchEmojis(query: string): typeof EMOJI_DATA {
  if (!query.trim()) return EMOJI_DATA;
  const q = query.toLowerCase();
  return EMOJI_DATA.map((category) => {
    const labels = EMOJI_SEARCH_LABELS[category.label] ?? [];
    const categoryMatches = category.label.toLowerCase().includes(q) || labels.some((l) => l.includes(q));
    if (categoryMatches) return category;
    return { ...category, emojis: [] };
  }).filter((c) => c.emojis.length > 0);
}

export function searchLucideIcons(query: string): typeof LUCIDE_ICON_CATEGORIES {
  if (!query.trim()) return LUCIDE_ICON_CATEGORIES;
  const q = query.toLowerCase();
  return LUCIDE_ICON_CATEGORIES.map((category) => {
    const filteredIcons = category.icons.filter((name) =>
      name.toLowerCase().includes(q) || name.replace(/-/g, " ").includes(q)
    );
    return { ...category, icons: filteredIcons };
  }).filter((c) => c.icons.length > 0);
}
