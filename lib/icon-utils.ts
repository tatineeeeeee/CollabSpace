// --- Icon type detection ---

export function isLucideIcon(value: string): boolean {
  return value.startsWith("lucide:");
}

export function isUrlIcon(value: string): boolean {
  return value.startsWith("http://") || value.startsWith("https://");
}

export function isEmojiIcon(value: string): boolean {
  return !!value && !isLucideIcon(value) && !isUrlIcon(value);
}

export function getLucideIconName(value: string): string {
  return value.replace("lucide:", "");
}

